function createSizeVariants(itemIds: string[], prices: { small: number; medium: number; large: number }) {
  return itemIds.flatMap((itemId) => [
    {
      id: `var_${itemId}_small`,
      itemId,
      name: "Small",
      price: prices.small,
      sortOrder: 1,
      createdAt: seededAt,
    },
    {
      id: `var_${itemId}_medium`,
      itemId,
      name: "Medium",
      price: prices.medium,
      sortOrder: 2,
      createdAt: seededAt,
    },
    {
      id: `var_${itemId}_large`,
      itemId,
      name: "Large",
      price: prices.large,
      sortOrder: 3,
      createdAt: seededAt,
    },
  ]);
}

