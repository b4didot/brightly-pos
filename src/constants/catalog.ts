function linkModifier(modifierId: string, itemIds: string[]) {
  return itemIds.map((itemId) => ({
    id: `im_${itemId}_${modifierId}`,
    itemId,
    modifierId,
  }));
}

export const seedItemModifiers: ItemModifier[] = [
  ...linkModifier("mod_temperature", [...coffeeDrinkIds, ...teaDrinkIds]),
  ...linkModifier("mod_sugar_level", [...sweetenedCoffeeDrinkIds, ...teaDrinkIds]),
];

function linkAddOns(addOnItemIds: string[], itemIds: string[]) {
  return itemIds.flatMap((itemId) =>
    addOnItemIds.map((addOnItemId) => ({
      id: `iao_${itemId}_${addOnItemId}`,
      itemId,
      addOnItemId,
    })),
  );
}

